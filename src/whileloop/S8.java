package whileloop;

public class S8
 {
	public static void main(String[] args)         
	{
		int i=60;
		while(i<=75)
		{                                         //print frm 50-75 increment
			System.out.println(i);
			i++;
		}
		
		System.out.println("---------------------");
		
		int j=20;
		while(j>=13)
		{                                          //print frm 25-11 decrement
			System.out.println(j);
			j--;
		}
		
	}

}
