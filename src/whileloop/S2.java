package whileloop;

public class S2
{
	public static void main(String[] args)         
	{
		int i=50;
		while(i<=75)
		{                                         //print frm 50-75 increment
			System.out.println(i);
			i++;
		}
		
		int j=25;
		while(j>=11)
		{                                          //print frm 25-11 decrement
			System.out.println(j);
			j--;
		}
		
	}

}
