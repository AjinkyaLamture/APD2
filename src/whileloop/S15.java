package whileloop;

public class S15
 {
	public static void main(String[] args)     //10-4 print even nos...
	 {
		int i=8;
		while(i>=2)
		{
			System.out.println(i*i*i);
			i=i-2;
		}
		System.out.println("----------");
		
		int j=2;
		while(j<=8)
		{
			System.out.println(j*j);
			j=j+2;
		}
		
	}

}
